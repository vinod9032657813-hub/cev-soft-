import mongoose from 'mongoose';

const orderschema = new mongoose.Schema({
  userId: {
    type: String,
    required: false
  },
  items: {
    type: Array,
    required: true
  },
  amount: {
    type: Number,
    required: true
  },
  address: {
    type: Object,
    required: true
  },
  status: {
    type: String,
    required: true,
    default: 'Order Placed'
  },
  paymentmethod: {
    type: String,
    required: true
  },
  payment: {
    type: Boolean,
    required: true,
    default: false
  },
  date: {
    type: Number,
    required: true
  }
}, { timestamps: true });

const Order = mongoose.model('Order', orderschema);

export default Order;